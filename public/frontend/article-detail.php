                        <h1>How will One Health approach protect Indonesia against future pandemics?</h1>

                        <p>Emerging infectious diseases (EIDs) at the human-animal-ecosystems interface have been occurring with increasing frequency. The emergence of COVID-19 has resulted in a global pandemic with significant mortality and a global economic recession.</p> 

                        <p>In addition, zoonoses (diseases transmissible from animals to humans) pose an ongoing threat to human health and a high cost to public health systems. Zoonoses can be spread by direct transmission (plague, anthrax, brucellosis), indirect transmission (leptospirosis, zika) or are food borne (salmonella, E.coli).</p> 

                        <p>Many zoonoses are regarded as ‘neglected diseases’ and though causing significant ill health and morbidity, the diseases receive little attention from public health authorities (various parasitic diseases in particular, such as hydatids, cysticercosis). In addition to EIDs there is also a growing threat from the increase in antimicrobial resistance (AMR), a problem for both human and animal health.</p>

                        <p>To address the threats from emerging infectious diseases, food-borne zoonoses, and to reduce the risks from AMR there is a need to take a broad based, holistic approach – the ‘One Health’ approach –, to disease prevention, detection, response and recovery.</p>

                        <p>The One Health approach identifies the need for the human health, animal health (both domesticated and wild animals) and environmental health sectors to work together using cross-sectoral, multi and inter-disciplinary approaches. Further, One Health needs to work within the social, cultural and economic context of countries adopting a broad approach, including Indonesia. The COVID-19 pandemic has emphasized the need for effective One Health systems to be established nationally and globally.</p>

                        <p>The Australia Indonesia Health Security Partnership (AIHSP) with the Food and Agriculture Organization (FAO), World Health Organization (WHO) and in close collaboration with the Government of Indonesia, particularly Ministry of Health (MoH), Ministry of Agriculture (MoA) and Ministry of Environment and Forestry (MoEF), is recognizing the importance of One Health, promoting awareness and supporting for the implementation of One Health approach in Indonesia through a series of activities.</p>

                        <p>One of the key activities was the webinar titled “Preventing the next pandemic-the One Health imperative’ last November 2020.</p>

